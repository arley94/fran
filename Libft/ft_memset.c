#include "libft.h"

void	*ft_memset(void *dest, int c, size_t count)
{
	unsigned char	*destiny;
	size_t			i;

	destiny = dest;
	i = 0;
	while (i < count)
	{
		destiny[i] = (unsigned char)c;
		i++;
	}
	return (destiny);
}
